/*
      java.lang.String - Immutable (Can not be modified)
 
      java.lang.StringBuffer - Mutable (Can be modified) 
*/
   
    public class StringDemo
    {
        public static void main(String [] args)
        {
             String s1 = "Computer";
             String s2 = new String("Computer"); 

             System.out.println("\n s1 = "+s1);
             System.out.println("\n s2 = "+s2);
        }
    }


